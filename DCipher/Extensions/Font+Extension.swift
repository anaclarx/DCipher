import SwiftUI

extension Font {
    static func fliegeMonoRegular(size: CGFloat) -> Font {
        .custom("FliegeMono-Regular", size: size)
    }

    static func fliegeMonoMedium(size: CGFloat) -> Font {
        .custom("FliegeMono-Medium", size: size)
    }
}
